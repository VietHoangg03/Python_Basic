a = input("Nhập vào số a: ");
a = float(a)
b = input("Nhập vào số b: ");
b = float(b)

print("{0} + {1} = {2}".format(a,b,a+b));
print("{0} - {1} = {2}".format(a,b,a-b));
print("{0} * {1} = {2}".format(a,b,a*b));
print("{0} : {1} = {2}".format(a,b,a/b));
print("{0} % {1} = {2}".format(a,b,a%b));
print("{0} ** {1} = {2}".format(a,b,a**b));
print("{0} // {1} = {2}".format(a,b,a//b));